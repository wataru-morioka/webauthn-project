@Component
export default class AboutContent extends Vue {
  private test: string = '../assets/内定書.pdf';

  private mounted() {
    this.fadein();
    $('.content').scroll(() => {
      this.fadein();
    });
  }

  private fadein(): void {
    const offset =  200;
    const effectPos = $(window).height()! - offset;
    $('.column').each( function() {
      const thisPos = $(this).offset()!.top;
      if ( effectPos > thisPos ) {
        $(this).css({
            opacity: 1,
            transform: 'translate(0px, 0px)',
        });
      } else {
        $(this).css({
            'opacity': 0,
            'transition-delay': '0s',
        });
      }
    });
  }
}
